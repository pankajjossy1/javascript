//Celcius to Farenheit and vice versa

//javascript code to convert celcius to fahrenheit and vice-versa

var celciusTemp = 30, farTemp=130;
var celciusToFar, farToCelcius;
celciusToFar = (5 / 9) * (farTemp - 32);
farToCelcius = (celciusTemp * 9) / 5 + 32;
console.log(`Celcius Temperature = ${celciusToFar}`);
console.log(`Farenheit Temperature ${farToCelcius}`);

